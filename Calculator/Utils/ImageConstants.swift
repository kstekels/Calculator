import Foundation
import SwiftUI

enum Colors: String {
    case buttonBackground
    case buttonBg
    case foregroundDigits
    case foregroundRightButtons
    case ForegroundTopButtons
    case PrimaryBg
    case SecondaryBackground
    case sunOrMoonNotSelected
    case sunOrMoonSelected
}


let buttonBackground = Color(Colors.buttonBackground.rawValue)
let buttonBg = Color(Colors.buttonBg.rawValue)
let foregroundDigits = Color(Colors.foregroundDigits.rawValue)
let foregroundRightButtons = Color(Colors.foregroundRightButtons.rawValue)
let ForegroundTopButtons = Color(Colors.ForegroundTopButtons.rawValue)
let PrimaryBg = Color(Colors.PrimaryBg.rawValue)
let SecondaryBackground = Color(Colors.SecondaryBackground.rawValue)
let sunOrMoonNotSelected = Color(Colors.sunOrMoonNotSelected.rawValue)
let sunOrMoonSelected = Color(Colors.sunOrMoonSelected.rawValue)
